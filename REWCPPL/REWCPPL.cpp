rew::AABB::AABB(rew::AABB6f b)
{
	center = rew::Vector3((b.min_x + b.max_x) * 0.5f, (b.min_y + b.max_y) * 0.5f, (b.min_z + b.max_z) * 0.5f);
	half_length = rew::Vector3((b.max_x - b.min_x) * 0.5f, (b.max_y - b.min_y) * 0.5f, (b.max_z - b.min_z) * 0.5f);
}

void rew::AABB6f::ExtendTo(float newx, float newy, float newz)
{
	if (newx > max_x)max_x = newx;
	if (newy > max_y)max_y = newy;
	if (newz > max_z)max_z = newz;

	if (newx < min_x) min_x = newx;
	if (newy < min_y) min_y = newy;
	if (newz < min_z) min_z = newz;
}


